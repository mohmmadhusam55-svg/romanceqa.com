// Language management system
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('selectedLanguage') || 'en';
        this.init();
    }
    
    init() {
        this.loadLanguage(this.currentLanguage);
        this.setupLanguageToggle();
        this.updateLanguageButton();
    }
    
    loadLanguage(lang) {
        if (!translations[lang]) {
            lang = 'en'; // Fallback to English
        }
        
        this.currentLanguage = lang;
        localStorage.setItem('selectedLanguage', lang);
        
        // Update all elements with data-lang-key attribute
        document.querySelectorAll('[data-lang-key]').forEach(element => {
            const key = element.getAttribute('data-lang-key');
            const translation = translations[lang][key];
            if (translation) {
                if (element.tagName === 'INPUT' && element.type === 'search') {
                    element.placeholder = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });
        
        // Update page direction for Arabic
        if (lang === 'ar') {
            document.documentElement.setAttribute('dir', 'rtl');
            document.documentElement.setAttribute('lang', 'ar');
        } else {
            document.documentElement.removeAttribute('dir');
            document.documentElement.setAttribute('lang', lang);
        }
        
        // Update language button text
        this.updateLanguageButton();
    }
    
    updateLanguageButton() {
        const langButton = document.querySelector('.lang-btn span');
        if (langButton) {
            const langNames = {
                'en': 'English',
                'tr': 'Türkçe',
                'ar': 'العربية'
            };
            langButton.textContent = langNames[this.currentLanguage] || 'English';
        }
    }
    
    setupLanguageToggle() {
        const langToggle = document.getElementById('header-lang-toggle');
        const langDropdown = document.getElementById('lang-dropdown');
        
        if (langToggle && langDropdown) {
            langToggle.addEventListener('click', (e) => {
                e.stopPropagation();
                langDropdown.style.display = langDropdown.style.display === 'block' ? 'none' : 'block';
            });
            
            // Handle language selection
            langDropdown.addEventListener('click', (e) => {
                if (e.target.classList.contains('lang-option')) {
                    e.preventDefault();
                    const selectedLang = e.target.getAttribute('data-lang');
                    this.loadLanguage(selectedLang);
                    langDropdown.style.display = 'none';
                    
                    // Update active state
                    langDropdown.querySelectorAll('.lang-option').forEach(option => {
                        option.classList.remove('active');
                    });
                    e.target.classList.add('active');
                }
            });
            
            // Close dropdown when clicking outside
            document.addEventListener('click', () => {
                langDropdown.style.display = 'none';
            });
        }
        
        // Setup navigation active states
        this.setupNavigationActiveStates();
    }
    
    setupNavigationActiveStates() {
        const navLinks = document.querySelectorAll('.nav-link, .icon-link');
        
        navLinks.forEach(link => {
            link.addEventListener('click', function(e) {
                console.log('Clicked link:', this.getAttribute('href'));
                
                // Remove active class from all nav links
                document.querySelectorAll('.nav-link, .icon-link').forEach(navLink => {
                    navLink.classList.remove('active');
                });
                
                // Add active class to clicked link
                this.classList.add('active');
                
                console.log('Active class added to:', this.getAttribute('href'));
            });
        });
        
        // Set active state based on current page
        this.setCurrentPageActive();
    }
    
    setCurrentPageActive() {
        const currentPath = window.location.pathname;
        const currentPage = currentPath.split('/').pop() || 'index.html';
        const navLinks = document.querySelectorAll('.nav-link, .icon-link');
        
        console.log('Current page:', currentPage);
        
        navLinks.forEach(link => {
            const href = link.getAttribute('href');
            let isActive = false;
            
            // Check for exact matches first
            if (href === currentPath) {
                isActive = true;
            }
            // Check for page-specific matches
            else if (currentPage === 'retail.html' && href.includes('retail')) {
                isActive = true;
            }
            else if (currentPage === 'wholesale.html' && href.includes('wholesale')) {
                isActive = true;
            }
            else if (currentPage === 'basket.html' && href.includes('basket')) {
                isActive = true;
            }
            // Check for home page (index.html or root) - handle both index.html and index.html#home
            else if ((currentPage === 'index.html' || currentPage === '' || currentPath === '/') && 
                     (href.includes('index.html') || href === 'index.html' || href === 'index.html#home')) {
                isActive = true;
            }
            
            if (isActive) {
                console.log('Setting active for:', href);
                // Remove active from all links first
                navLinks.forEach(navLink => navLink.classList.remove('active'));
                
                // Add active to matching link
                link.classList.add('active');
            }
        });
    }
    
    getTranslation(key) {
        return translations[this.currentLanguage][key] || key;
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.languageManager = new LanguageManager();
});

